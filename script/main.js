let insulationButton = document.getElementById('insulation');
let acousticButton = document. getElementById('acoustic');
let headerText = document.querySelector('.header_text');
let constrText = document.querySelector('.constr_text');
let formConstr = document.querySelector('form');

let flag = 1;
insulationButton.onclick = function(){
    if(flag == 1) goToConstrTypes();
    else if( flag == 2) returnToMainMenu();
    
}
function goToConstrTypes(){
    acousticButton.style.display = 'none';
    headerText.style.display = 'none';
    formConstr.style.position = 'block';
    formConstr.style.display = 'block';
    constrText.style.display = 'block';
    flag = 2;
}
function returnToMainMenu(){
    acousticButton.style.display = 'block';
    headerText.style.display = 'block';
    formConstr.style.position = 'block';
    formConstr.style.display = 'none';
    constrText.style.display = 'none';
    flag = 1;
}  

acousticButton.onclick = function(){
    headerText.style.display = 'none';
    acousticButton.style.top = 'auto';
    acousticButton.style.marginLeft = 'auto';
    acousticButton.style.position = 'relative';
    formConstr.style.display = 'none';
    constrText.style.display = 'block';
    insulationButton.style.display = 'none';
}
